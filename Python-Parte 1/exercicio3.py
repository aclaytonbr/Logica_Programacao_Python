#Escreva um programa que peça ao usuário dois números inteiros e exiba a soma, 
#subtração, multiplicação e divisão desses números.

n1 = int(input("Informe um número inteiro: "))
n2 = int(input("Informe outro número inteiro: "))

print(f'{n1} + {n2} = {n1 + n2}')
print(f'{n1} - {n2} = {n1 - n2}')
print(f'{n1} * {n2} = {n1 * n2}')
print(f'{n1} / {n2} = {n1 / n2}')